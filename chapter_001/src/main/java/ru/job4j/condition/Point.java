public class Point {
    private int x;
    private int y;

    public Point() {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {

        return this.y;
    }

    public boolean is(int a, int b) {

        return y == a * x + b;
    }

}
